    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        if (ListaDB.getSelectedRow() >= 0) {
            ProprietarioMicro selecionadoProprietarioMicro = new ProprietarioMicro();
            selecionadoProprietarioMicro = listaArray.get(ListaDB.getSelectedRow());

            AlterarProprietarioMicro alterarProprietarioMicro = new AlterarProprietarioMicro(selecionadoProprietarioMicro, this.logado);
            alterarProprietarioMicro.setVisible(true);
            dispose();

        } else {
            JOptionPane.showMessageDialog(null, "Escolha algum Proprietário listado na tabela para alterá-lo!");
        };
    }//GEN-LAST:event_jButtonAlterarActionPerformed

    ArrayList<ProprietarioMicro> listaArray;

    private void jButtonListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarActionPerformed

        try {

            ProprietarioMicro filtroProprietarioMicro = new ProprietarioMicro();

            if (campoListar.getText().trim().equals("") == false) {
                filtroProprietarioMicro.setProprietario(campoListar.getText());
            } else {
                filtroProprietarioMicro.setProprietario("");
            }

            listaArray = Fachada.getInstancia().listarProprietarioMicro(filtroProprietarioMicro);

            DefaultTableModel tabela = new DefaultTableModel();
            tabela.setColumnIdentifiers(new String[]{"Descrição do Proprietário"});

            for (ProprietarioMicro este : listaArray) {
                tabela.addRow(new String[]{este.getProprietario()});
            }

            ListaDB.setModel(tabela);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Erro ao listar - " + e.getMessage());
        }

    }//GEN-LAST:event_jButtonListarActionPerformed

    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed

        if (ListaDB.getSelectedRow() >= 0) {
            ProprietarioMicro removerProprietarioMicro = new ProprietarioMicro();
            removerProprietarioMicro = listaArray.get(ListaDB.getSelectedRow());

            if (JOptionPane.showConfirmDialog(new JFrame(),
                    "Tem certeza que deseja remover este Proprietário: " + removerProprietarioMicro.getProprietario() + "? \n(É necessário que nenhum usuário possua equipamentos deste Proprietario)",
                    "Remover Proprietário de Micro", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                try {

                    int selectedrow = (ListaDB.getSelectedRow());
                    Fachada.getInstancia().removerProprietarioMicro(removerProprietarioMicro);

                    JOptionPane.showMessageDialog(null, "Proprietario de Micro removido com sucesso!");

                    listaArray.remove(selectedrow);
                    DefaultTableModel tabelaDeMarcaMicro = new DefaultTableModel();
                    tabelaDeMarcaMicro.setColumnIdentifiers(new String[]{"Descrição do Proprietário"});
                    //varrendo o ArrayList de Funcionário e colocando cada objeto funcionário em uma linha
                    for (ProprietarioMicro este : listaArray) {
                        tabelaDeMarcaMicro.addRow(new String[]{este.getProprietario()});
                    }

                    ListaDB.setModel(tabelaDeMarcaMicro);

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            } else {

                JOptionPane.showMessageDialog(rootPane, "Operação cancelada.");
                return;
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Escolha algum Proprietário da lista para removê-lo.");
        }
    }//GEN-LAST:event_jButtonRemoverActionPerformed

