    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        if (TodasMarcas.getSelectedRow() >= 0) {
            MarcaMicro selecionadoMarcaMicro = new MarcaMicro();
            selecionadoMarcaMicro = listaDeMarcas.get(TodasMarcas.getSelectedRow());

            AlterarMarcaMicro alterarMarcaMicro = new AlterarMarcaMicro(selecionadoMarcaMicro, this.logado);
            alterarMarcaMicro.setVisible(true);
            dispose();

        } else {
            JOptionPane.showMessageDialog(null, "Escolha alguma Marca listada na tabela para alterá-la!");
        };
    }//GEN-LAST:event_jButtonAlterarActionPerformed

    ArrayList<MarcaMicro> listaDeMarcas;

    private void jButtonListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarActionPerformed

        try {

            MarcaMicro filtroMarcaMicro = new MarcaMicro();

            if (campoListarMarca.getText().trim().equals("") == false) {
                filtroMarcaMicro.setMarca(campoListarMarca.getText());
            } else {
                filtroMarcaMicro.setMarca("");
            }

            filtroMarcaMicro.setIdMarca(0);

            listaDeMarcas = Fachada.getInstancia().listarMarcaMicro(filtroMarcaMicro);

            DefaultTableModel tabelaDeMarcas = new DefaultTableModel();
            tabelaDeMarcas.setColumnIdentifiers(new String[]{"Descrição da Marca"});

            for (MarcaMicro marca : listaDeMarcas) {
                tabelaDeMarcas.addRow(new String[]{marca.getMarca()});
            }

            TodasMarcas.setModel(tabelaDeMarcas);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Erro ao listar - " + e.getMessage());
        }

    }//GEN-LAST:event_jButtonListarActionPerformed

    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed

        if (TodasMarcas.getSelectedRow() >= 0) {
            MarcaMicro removerMarcaMicro = new MarcaMicro();
            removerMarcaMicro = listaDeMarcas.get(TodasMarcas.getSelectedRow());

            if (JOptionPane.showConfirmDialog(new JFrame(),
                    "Tem certeza que deseja remover esta Marca (" + removerMarcaMicro.getMarca() + ")? \n(É necessário que nenhum usuário possua este tipo de Marca)",
                    "Remover Marca de Micro", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                try {

                    int selectedrow = (TodasMarcas.getSelectedRow());
                    Fachada.getInstancia().removerMarcaMicro(removerMarcaMicro);

                    JOptionPane.showMessageDialog(null, "Marca removida com sucesso!");

                    listaDeMarcas.remove(selectedrow);
                    DefaultTableModel tabelaDeMarcaMicro = new DefaultTableModel();
                    tabelaDeMarcaMicro.setColumnIdentifiers(new String[]{"Descrição da Marca"});
                    //varrendo o ArrayList de Funcionário e colocando cada objeto funcionário em uma linha
                    for (MarcaMicro marca : listaDeMarcas) {
                        tabelaDeMarcaMicro.addRow(new String[]{marca.getMarca()});
                    }

                    TodasMarcas.setModel(tabelaDeMarcaMicro);

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            } else {

                JOptionPane.showMessageDialog(rootPane, "Operação cancelada.");
                return;
            }
        } else {

            JOptionPane.showMessageDialog(rootPane, "Escolha alguma Marca da lista para removê-la.");
        }
    }//GEN-LAST:event_jButtonRemoverActionPerformed

