package Micro.OS;

/**
 *
 * @author Posseidon
 */
public class OS {
    private int idOS;
    private String os;

    /**
     * @return the os
     */
    public String getOs() {
        return os;
    }

    /**
     * @param os the os to set
     */
    public void setOs(String os) {
        this.os = os;
    }

    /**
     * @return the idOS
     */
    public int getIdOS() {
        return idOS;
    }

    /**
     * @param idOS the idOS to set
     */
    public void setIdOS(int idOS) {
        this.idOS = idOS;
    }

}
