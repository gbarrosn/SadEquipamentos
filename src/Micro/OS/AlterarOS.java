    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed

        try {
            OS alterarOS = new OS();
            alterarOS.setOs(campoDescr.getText());

            Fachada.getInstancia().alterarOS(alterarOS, selecionadoOS);

            JOptionPane.showMessageDialog(rootPane, "Sucesso ao alterar Sistema Operacional!\nDe: " + selecionadoOS.getOs() + ". \nPara: " + alterarOS.getOs() + ".");
            ListarOS listar = new ListarOS(this.logado);
            listar.setVisible(true);
            dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao alterar - " + ex.getMessage());
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

