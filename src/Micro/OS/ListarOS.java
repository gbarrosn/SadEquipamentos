    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        if (ListaDB.getSelectedRow() >= 0) {
            OS selecionadoOS = new OS();
            selecionadoOS = listaArray.get(ListaDB.getSelectedRow());

            AlterarOS alterarOS = new AlterarOS(selecionadoOS, this.logado);
            alterarOS.setVisible(true);
            dispose();

        } else {
            JOptionPane.showMessageDialog(null, "Escolha algum Sistema listado na tabela para alterá-lo");
        };
    }//GEN-LAST:event_jButtonAlterarActionPerformed

    ArrayList<OS> listaArray;

    private void jButtonListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarActionPerformed

        try {

            OS filtroOS = new OS();

            if (campoListar.getText().trim().equals("") == false) {
                filtroOS.setOs(campoListar.getText());
            } else {
                filtroOS.setOs("");
            }

            listaArray = Fachada.getInstancia().listarOS(filtroOS);

            DefaultTableModel tabela = new DefaultTableModel();
            tabela.setColumnIdentifiers(new String[]{"Descrição do Sistema Operacional"});

            for (OS este : listaArray) {
                tabela.addRow(new String[]{este.getOs()});
            }

            ListaDB.setModel(tabela);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Erro de conexão ao banco de Dados - " + e.getMessage());
        }

    }//GEN-LAST:event_jButtonListarActionPerformed

    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed
        if (ListaDB.getSelectedRow() >= 0) {
            OS removerOS = new OS();
            removerOS = listaArray.get(ListaDB.getSelectedRow());

            if (JOptionPane.showConfirmDialog(new JFrame(),
                    "Tem certeza que deseja remover este S.O. (" + removerOS.getOs() + ")? \n(É necessário que nenhum usuário possua este tipo de S.O.)",
                    "Remover Sistema Operacional", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                try {

                    int selectedrow = (ListaDB.getSelectedRow());
                    Fachada.getInstancia().removerOS(removerOS);

                    JOptionPane.showMessageDialog(null, "Sistema Operacional removido com sucesso!");

                    listaArray.remove(selectedrow);
                    DefaultTableModel tabela = new DefaultTableModel();
                    tabela.setColumnIdentifiers(new String[]{"Descrição do Sistema Operacional"});
                    //varrendo o ArrayList de Funcionário e colocando cada objeto funcionário em uma linha
                    for (OS este : listaArray) {
                        tabela.addRow(new String[]{este.getOs()});
                    }

                    ListaDB.setModel(tabela);

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            } else {

                JOptionPane.showMessageDialog(rootPane, "Operação cancelada.");
                return;
            }

        } else {

            JOptionPane.showMessageDialog(rootPane, "Escolha algum SO da lista para removê-lo.");
        }

    }//GEN-LAST:event_jButtonRemoverActionPerformed

