    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed

        try {
            PlataformaMicro alterarPlataforma = new PlataformaMicro();
            alterarPlataforma.setPlataforma(campoDescr.getText());

         //DadosConfiguracao dadosConfig = new DadosConfiguracao();
            //dadosConfig.alterarConfiguracao(alterarConfig, selecionadoConfig);
            Fachada.getInstancia().alterarPlataformaMicro(alterarPlataforma, selecionadoPlataforma);

            JOptionPane.showMessageDialog(rootPane, "Sucesso ao alterar Plataforma! \nDe: " + selecionadoPlataforma.getPlataforma() + ". \nPara: " + alterarPlataforma.getPlataforma() + ".");
            ListarPlataforma listar = new ListarPlataforma(this.logado);
            listar.setVisible(true);
            dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao alterar - " + ex.getMessage());
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

