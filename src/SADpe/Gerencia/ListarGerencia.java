    private ArrayList<Pavimento> allPavimentos;

    private void comboBoxListagem() {
        try {

            try {
                Pavimento comboPavimento = new Pavimento();

                comboPavimento.setPavimento("");

                allPavimentos = Fachada.getInstancia().listarPavimento(comboPavimento);

                DefaultComboBoxModel listaComboPavimento = new DefaultComboBoxModel();

                listaComboPavimento.addElement("Pavimento");

                for (int i = 0; i < allPavimentos.size(); i++) {
                    Pavimento nomeComboPavimento = new Pavimento();
                    nomeComboPavimento = allPavimentos.get(i);
                    listaComboPavimento.addElement(nomeComboPavimento.getPavimento());
                }
                Box_PavimentoUser.setModel(listaComboPavimento);

            } catch (Exception e) {
                throw new Exception("Não existe nenhumo Pavimento cadastrado no banco de dados.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar opções do Combo Box.");
        }
    }

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        if (ListaDB.getSelectedRow() >= 0) {
            Gerencia selecionadoGerencia = new Gerencia();
            selecionadoGerencia = listaArray.get(ListaDB.getSelectedRow());

            AlterarGerencia alterarGerencia = new AlterarGerencia(selecionadoGerencia, this.logado);
            alterarGerencia.setVisible(true);
            dispose();

        } else {
            JOptionPane.showMessageDialog(null, "Escolha alguma linha da tabela para alterar");
        };
    }//GEN-LAST:event_jButtonAlterarActionPerformed

    ArrayList<Gerencia> listaArray;

    private void jButtonListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarActionPerformed

        try {

            Gerencia filtroGerencia = new Gerencia();

            if (campoListar.getText().trim().equals("") == false) {
                filtroGerencia.setGerencia(campoListar.getText());
            } else {
                filtroGerencia.setGerencia("");
            }
            if (campoListarsigla.getText().trim().equals("") == false) {
                filtroGerencia.setSiglaGerencia(campoListarsigla.getText());
            } else {
                filtroGerencia.setSiglaGerencia("");
            }

            //FILTRO PAVIMENTO
            if (Box_PavimentoUser.getSelectedItem().equals("Pavimento")) {
                Pavimento vazio = new Pavimento();
                vazio.setIdPavimento(0);
                vazio.setPavimento("");
                filtroGerencia.setPavimento(vazio);

            } else {
                int SelecionadoPavimento = Box_PavimentoUser.getSelectedIndex() - 1;
                filtroGerencia.setPavimento(allPavimentos.get(SelecionadoPavimento));

            }

            listaArray = Fachada.getInstancia().listarGerencia(filtroGerencia);

            DefaultTableModel tabela = new DefaultTableModel();
            tabela.setColumnIdentifiers(new String[]{"Pavimento", "Descrição da Gerência", "Sigla"});

            for (Gerencia este : listaArray) {
                tabela.addRow(new String[]{este.getPavimento().getPavimento(), este.getGerencia(), este.getSiglaGerencia()});

            }

            ListaDB.setModel(tabela);

            ListaDB.getColumnModel().getColumn(0).setMinWidth(80);
            ListaDB.getColumnModel().getColumn(0).setMaxWidth(180);
            ListaDB.getColumnModel().getColumn(2).setMinWidth(180);
            ListaDB.getColumnModel().getColumn(2).setMaxWidth(300);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Erro de conexão ao banco de Dados - " + e.getMessage());
        }

    }//GEN-LAST:event_jButtonListarActionPerformed

    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed

        if (ListaDB.getSelectedRow() >= 0) {
            Gerencia removerGerencia = new Gerencia();
            removerGerencia = listaArray.get(ListaDB.getSelectedRow());

            if (JOptionPane.showConfirmDialog(new JFrame(),
                    "Tem certeza que deseja remover esta Gerência (" + removerGerencia.getGerencia() + ")? \n(É necessário que nenhum usuário pertença a esta Gerência)",
                    "Remover Gerência", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                try {

                    int selectedrow = (ListaDB.getSelectedRow());
                    Fachada.getInstancia().removerGerencia(removerGerencia);

                    JOptionPane.showMessageDialog(null, "Gerência removida com sucesso!");

                    listaArray.remove(selectedrow);

                    DefaultTableModel tabela = new DefaultTableModel();
                    tabela.setColumnIdentifiers(new String[]{"Descrição da Gerência", "Sigla"});

                    for (Gerencia este : listaArray) {
                        tabela.addRow(new String[]{este.getGerencia(), este.getSiglaGerencia()});

                    }

                    ListaDB.setModel(tabela);

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "Operação cancelada.");
                return;
            }

        } else {

            JOptionPane.showMessageDialog(rootPane, "Escolha alguma Gerência da lista para remover.");
        }

    }//GEN-LAST:event_jButtonRemoverActionPerformed

