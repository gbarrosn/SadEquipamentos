public class Pavimento {
    private int idPavimento;
    private String pavimento;
    

    /**
     * @return the pavimento
     */
    public String getPavimento() {
        return pavimento;
    }

    /**
     * @param pavimento the pavimento to set
     */
    public void setPavimento(String pavimento) {
        this.pavimento = pavimento;
    }

    /**
     * @return the idPavimento
     */
    public int getIdPavimento() {
        return idPavimento;
    }

    /**
     * @param idPavimento the idPavimento to set
     */
    public void setIdPavimento(int idPavimento) {
        this.idPavimento = idPavimento;
    }
    
}
