    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed

        try {
            Cargo alterarCargo = new Cargo();
            alterarCargo.setCargo(campoDescr.getText());

            Fachada.getInstancia().alterarCargo(alterarCargo, selecionadoCargo);

            JOptionPane.showMessageDialog(rootPane, "Cargo alterado com sucesso!");
            ListarCargo listar = new ListarCargo(this.logado);
            listar.setVisible(true);
            dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao alterar Cargo - " + ex.getMessage());
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

