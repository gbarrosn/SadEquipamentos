    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            LoginUser loginUser = new LoginUser();
            String low = campoLogin.getText();
            loginUser.setLogin(low.toLowerCase());
            String senhaPadrao = "sad";
            loginUser.setSenha(senhaPadrao.hashCode() + "");

            if (jCheckBoxAlmoxarifado.isSelected() == true) {
                loginUser.setAdm("ALMOXARIFADO"); //TODO adicionar o usuário do patrimônio
            } else {
                if (campoADM.isSelected() == true) {
                    loginUser.setAdm("SIM");
                } else {
                    loginUser.setAdm("NÃO");
                }
            }

            Fachada.getInstancia().verificaLogin(loginUser);
            Fachada.getInstancia().cadastrarLogin(loginUser);
            JOptionPane.showMessageDialog(rootPane, "Usuário: \"" + loginUser.getLogin() + "\" criado com sucesso!");

            if (JOptionPane.showConfirmDialog(new JFrame(),
                    "Deseja criar outro Usuário?",
                    "Criar Novo usuário", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                campoLogin.setText("");
                campoADM.setSelected(false);
                
            } else {
                //TelaPrincipal inicial = new TelaPrincipal(logado);
                
               // inicial.setVisible(true);
                dispose();
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao criar Usuário - " + ex.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

