            MarcaMonitor alterarMarcaMonitor = new MarcaMonitor();
            alterarMarcaMonitor.setMarca(campoDescrMarca.getText());

         //DadosConfiguracao dadosConfig = new DadosConfiguracao();
            //dadosConfig.alterarConfiguracao(alterarConfig, selecionadoConfig);
            Fachada.getInstancia().alterarMarcaMonitor(alterarMarcaMonitor, selecionadoMarcaMonitor);

            JOptionPane.showMessageDialog(rootPane, "Marca de Monitor alterada com sucesso.");
