    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed

        try {

            ProprietarioMonitor alterarProprietarioMonitor = new ProprietarioMonitor();
            alterarProprietarioMonitor.setProprietario(campoDescr.getText());

            Fachada.getInstancia().alterarProprietarioMonitor(alterarProprietarioMonitor, selecionadoProprietarioMonitor);

            JOptionPane.showMessageDialog(rootPane, "Sucesso ao alterar Proprietario de Monitor! \nDe: " + selecionadoProprietarioMonitor.getProprietario() + ". \nPara: " + alterarProprietarioMonitor.getProprietario() + ".");
            ListarProprietarioMonitor listar = new ListarProprietarioMonitor(this.logado);
            listar.setVisible(true);
            dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao alterar - " + ex.getMessage());
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

