    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
       
        try{
        ProprietarioMonitor proprietarioMonitor = new ProprietarioMonitor();
        proprietarioMonitor.setProprietario(campoDescr.getText());
        
        Fachada.getInstancia().cadastrarProprietarioMonitor(proprietarioMonitor);
        JOptionPane.showMessageDialog(rootPane, "Proprietário: \""+proprietarioMonitor.getProprietario()+"\" cadastrado com sucesso!");
        
         if ( JOptionPane.showConfirmDialog(new JFrame(), 
       "Deseja cadastrar outro Proprietario?", 
       "Cadastrar Novo Proprietario de Monitor", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) { 
            campoDescr.setText("");
            }else{
                ListarProprietarioMonitor inicial= new ListarProprietarioMonitor(this.logado);  
                inicial.setVisible(true);
                dispose();
            }
        
        }
        catch (Exception ex){
            JOptionPane.showMessageDialog(rootPane, "Erro ao cadastrar - "+ ex.getMessage());
        }
        
        
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

