    private void jMenuItemConsultarFornActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemConsultarFornActionPerformed
        try {

            listaForn = Fachada.getInstancia().listaEscolherForn();
            DefaultTableModel tabelaFornecedor = new DefaultTableModel() {
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            ;

            };

            tabelaFornecedor.setColumnIdentifiers(new String[]{"Cnpj", "Fornecedor"});
            TabelaFornecedor.getColumnModel().getColumn(0).setMaxWidth(160);
            TabelaFornecedor.getColumnModel().getColumn(1).setMaxWidth(380);

            for (Fornecedor forn : listaForn) {

                tabelaFornecedor.addRow(new String[]{forn.getCnpj(), forn.getNome()});

                TabelaFornecedor.setModel(tabelaFornecedor);
                TabelaFornecedor.getColumnModel().getColumn(0).setMaxWidth(160);
                TabelaFornecedor.getColumnModel().getColumn(1).setMaxWidth(380);

            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro:" + ex.getMessage());
        }
    }//GEN-LAST:event_jMenuItemConsultarFornActionPerformed

    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        NotaFiscal nfHp = new NotaFiscal();
        try {
            if(TabelaFornecedor.getSelectedRow() >=0){
                escolhido = listaForn.get(TabelaFornecedor.getSelectedRow());
            //TRATANDO CAMPO NOTA FISCAL
            try {
                nfHp.setNotaFiscal(Long.parseLong(jTextFieldNF.getText().trim()));
            } catch (NumberFormatException ee) {
                if (jTextFieldNF.getText().trim().equals("") == true) {
                    throw new Exception("O campo Nota Fiscal não pode ser vazio");
                } else {
                    throw new Exception("Preencha o campo Nota Fiscal apenas com numeros ");
                }
            }
            nfHp.setData(jTextFieldData.getText().trim().toUpperCase());
            //RECEBENDO OBJETO ESCOLHIDO DO TIPO FORNECEDOR                    
            nfHp.setCod_Forn(escolhido);
            
            
            
            Fachada.getInstancia().CadastrarNF(nfHp);
        int opcao = JOptionPane.showConfirmDialog(rootPane, "Nota Fiscal cadastrada com sucesso!\n"
                + "Você deseja inserir Itens a essa nota agora?");
        
        if(opcao == JOptionPane.YES_OPTION){
            TelaNovoItensDaNota tela = new TelaNovoItensDaNota(nfHp, this.logado);
            tela.setVisible(true);
            dispose();
           }
        if(opcao == JOptionPane.NO_OPTION || opcao == JOptionPane.CANCEL_OPTION){
            
        }
            
            
            
            }else{
                throw new Exception("Para cadastrar esta Nota, selecione o fornecedor da mesma na tabela");
            }
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, " Erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

