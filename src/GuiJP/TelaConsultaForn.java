    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        try {

            listaForn = Fachada.getInstancia().listaFornecedor();
            DefaultTableModel tabelaFornecedor = new DefaultTableModel() {
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            ;

            };

            tabelaFornecedor.setColumnIdentifiers(new String[]{"Cnpj", "Fornecedor"});
            TabelaFornecedor.getColumnModel().getColumn(0).setMaxWidth(160);
            TabelaFornecedor.getColumnModel().getColumn(1).setMaxWidth(380);

            for (Fornecedor forn : listaForn) {

                tabelaFornecedor.addRow(new String[]{forn.getCnpj(), forn.getNome()});

                TabelaFornecedor.setModel(tabelaFornecedor);
                TabelaFornecedor.getColumnModel().getColumn(0).setMaxWidth(160);
                TabelaFornecedor.getColumnModel().getColumn(1).setMaxWidth(380);

            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro:" + ex.getMessage());
        }
    }//GEN-LAST:event_jButtonConsultarActionPerformed

    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed
        if (TabelaFornecedor.getSelectedRow() >= 0) {
            Fornecedor removeForn;
            removeForn = listaForn.get(TabelaFornecedor.getSelectedRow());
            if (JOptionPane.showConfirmDialog(new JFrame(),
                    "Tem certeza que deseja remover o(a) (" + removeForn.getNome() + ")? (esta ação não poderá ser desfeita)",
                    "Remover este Equipamento", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                try {
                    int selectRow = (TabelaFornecedor.getSelectedRow());
                    Fachada.getInstancia().removerFornecedor(removeForn);
                    JOptionPane.showMessageDialog(rootPane, "Fornecedor foi removido com sucesso");
                    listaForn.remove(selectRow);
                    jButtonConsultar.doClick();

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Operação cancelada.");
                //return;
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Escolha um Fornecedor para ser removido");
        }
    }//GEN-LAST:event_jButtonRemoverActionPerformed

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        if (TabelaFornecedor.getSelectedRow() >= 0) {
            Fornecedor selecionado;
            selecionado = listaForn.get(TabelaFornecedor.getSelectedRow());

            TelaAlterarForn alterar = new TelaAlterarForn(selecionado, this.logado);
            alterar.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione o Equipamento da lista acima que deseja alterar");

        }

    }//GEN-LAST:event_jButtonAlterarActionPerformed

