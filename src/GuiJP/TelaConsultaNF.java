    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        try {

            listaNF = Fachada.getInstancia().listaNotaFiscal();
            DefaultTableModel tabelaNotaFiscal = new DefaultTableModel() {
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            ;

            };
        
         tabelaNotaFiscal.setColumnIdentifiers(new String[]{"Código", "Nota Fiscal", "Cnpj", "Fornecedor", "Data"});
            for (NotaFiscal nf : listaNF) {
                tabelaNotaFiscal.addRow(new String[]{nf.getCodigo() + "", nf.getNotaFiscal() + "", nf.getCod_Forn().getCnpj() + "", nf.getCod_Forn().getNome(), nf.getData()});

                TabelaNotaFiscal.setModel(tabelaNotaFiscal);
                TabelaNotaFiscal.getColumnModel().getColumn(0).setMaxWidth(50);
                TabelaNotaFiscal.getColumnModel().getColumn(1).setMaxWidth(90);
                TabelaNotaFiscal.getColumnModel().getColumn(2).setMaxWidth(140);
                TabelaNotaFiscal.getColumnModel().getColumn(3).setMaxWidth(390);
                TabelaNotaFiscal.getColumnModel().getColumn(4).setMaxWidth(80);

            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro:" + ex.getMessage());
        }

    }//GEN-LAST:event_jButtonConsultarActionPerformed

