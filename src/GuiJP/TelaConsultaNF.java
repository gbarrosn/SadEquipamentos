    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed
        if (TabelaNotaFiscal.getSelectedRow() >= 0) {
            NotaFiscal nota;
            nota = listaNF.get(TabelaNotaFiscal.getSelectedRow());
            if (JOptionPane.showConfirmDialog(new JFrame(),
                    "Tem certeza que deseja remover a Nota (" + nota.getNotaFiscal() + ")? (Esta ação não poderá ser desfeita)",
                    "Remover este Equipamento", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                try {
                    int selectRow = (TabelaNotaFiscal.getSelectedRow());
                    Fachada.getInstancia().RemoverNF(nota);
                    JOptionPane.showMessageDialog(rootPane, "A Nota " + nota.getCodigo() + " foi removida com sucesso");
                    listaNF.remove(selectRow);
                    jButtonConsultar.doClick();

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Operação cancelada.");
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione na tabela uma Nota para ser removida");
        }
    }//GEN-LAST:event_jButtonRemoverActionPerformed

