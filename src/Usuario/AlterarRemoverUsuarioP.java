    private void campoDefinirCampoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_campoDefinirCampoItemStateChanged
        if (campoDefinirCampo.getSelectedItem().equals("Nome")) {
            comboboxDefinirValor.setEnabled(false);
        } else if (campoDefinirCampo.getSelectedItem().equals("Host Name")) {
            comboboxDefinirValor.setEnabled(false);
        } else if (campoDefinirCampo.getSelectedItem().equals("Tombo do Micro")) {
            comboboxDefinirValor.setEnabled(false);
        } else if (campoDefinirCampo.getSelectedItem().equals("Série do Micro")) {
            comboboxDefinirValor.setEnabled(false);
        } else if (campoDefinirCampo.getSelectedItem().equals("CMTECH")) {
            comboboxDefinirValor.setEnabled(false);
        } else if (campoDefinirCampo.getSelectedItem().equals("Tombo do Monitor")) {
            comboboxDefinirValor.setEnabled(false);
        } else if (campoDefinirCampo.getSelectedItem().equals("Série do Monitor")) {
            comboboxDefinirValor.setEnabled(false);
        } else if (campoDefinirCampo.getSelectedItem().equals("Garantia")) {
            comboboxDefinirValor.setEnabled(false);
        }
    }//GEN-LAST:event_campoDefinirCampoItemStateChanged

